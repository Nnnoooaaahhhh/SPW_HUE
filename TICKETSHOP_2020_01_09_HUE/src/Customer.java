
public class Customer {

	String Geschlecht;
	int id;
	String Vorname;
	String Nachname;
	String Adresse;
	String pw;
	
	
	Customer(String Geschlecht, int id, String Vorname, String Nachname, String Adresse, String pw){
		this.Geschlecht=Geschlecht;
		this.id=id;
		this.Vorname=Vorname;
		this.Nachname=Nachname;
		this.Adresse=Adresse;
		this.pw=pw;
	}
	
}
